3. 制作の裏側とエピソード
RZAによる革命的なプロダクションとサンプリング・アート
「C.R.E.A.M.」のプロダクションは、ヒップホップにおけるサンプリング技術の歴史的な到達点の一つである。トラックは、ニューヨークのFirehouse StudioにおいてRZAの手によって構築された。西海岸のDr. DreがMoogのシンセサイザーを用いてクリアで重厚なベースラインを弾き直す手法をとっていたのに対し、RZAはAKAI MPCやE-mu SP-1200といったサンプラーを駆使し、埃にまみれた古いレコードから抽出した音の断片をコラージュする手法を極限まで洗練させた。
楽曲の背骨となっているのは、Isaac HayesとDavid Porterがプロデュースを手掛けたThe Charmelsの1967年のソウル楽曲「As Long As I've Got You」のオープニングを飾る、悲哀に満ちたピアノリフである。RZAはこの哀愁漂うメロディを執拗にループさせ、その下にOtis ReddingとCarla Thomasの楽曲「Tramp」から抽出した荒々しいドラムブレイクを重ね合わせた。
ここで最も特筆すべきRZAのプロデュース手腕は、「クオンタイズ（リズムの自動補正機能）」の意図的な欠如である。ドラムのタイミングを機械的に完璧に合わせるのではなく、あえてズレ（スウィング）を残すことで、人間の鼓動やストリートの不規則な足音のような、泥臭く生々しいグルーヴを生み出した。この不安定で埃っぽいビートの隙間に、Delfonicsの「Hey Love」からサンプリングしたかすかな音の断片や、Wu-Tang Clanの系列メンバーである60 Second Assassinが録音したバックグラウンドの呻き声（"oohs"）が加わることで、美しさと不穏さが同居する唯一無二の音響空間が構築されている。
録音時の裏話：「消えたライム」の謎と神がかった偶然
楽曲の後半、Inspectah Deckのヴァースにおいて、熱心なヒップホップリスナーの間で長年語り草となっているオーディオ・エンジニアリング上の興味深い逸話が存在する。彼はヴァースの中盤で「Trying to get a clutch at what I could not...（手に入れられないものを必死に掴もうとしていた）」とラップするが、文脈上「touch（触れる）」という言葉で韻を踏むはずの箇所が、突如として無音になっているのである。
長年、リスナーや批評家たちはこれを「決して触れることのできないストリートの夢や富を表現するための、意図的な芸術的空白（MF DOOMなどが好んで用いる高度なリリカルテクニック）」だと解釈し、その詩的な表現を絶賛してきた。しかし、後にボーカルのみのアカペラトラックを詳細に解析したオーディオエンジニアの指摘や制作陣の証言により、その衝撃的な裏話が明らかになっている。録音当時、スタジオで孤独にミックス作業を行っていたRZAは、Inspectah Deckの「touch」という言葉に対して、左右のチャンネルを音が飛び交いながら徐々に消えていく「ピンポン・ディレイ」という高度なステレオエフェクトを手動でかけようと試みていた。
しかし、当時はアナログテープからデジタル機材への過渡期であり、物理的なテープのループやフェーダーの手動操作に頼らざるを得ない低予算の制作環境であった。RZAがフェーダーを操作した際、ディレイの減衰効果（ディケイ）が極端に早くかかりすぎてしまい、最終的なマスター音源において「touch」という言葉の音量ごと完全に消え去ってしまったのである。結果的に、この機材操作の偶然のミス（あるいは限界）が、リリックが持つ絶望感と「手の届かない夢」というテーマと奇跡的なまでにリンクし、ヒップホップ史上最も美しく詩的な空白を生み出すこととなった。
